//Importer le package HTTP de node.js
const http = require("http");

//Importer app.js
const app = require("./app");

var cors = require("cors");
app.use(cors()); // Use this after the variable declaration

//Paramétrer le port avec set de Express
const normalizePort = (val) => {
  const port = parseInt(val, 10);

  if (isNaN(port)) {
    return val;
  }
  if (port >= 0) {
    return port;
  }
  return false;
};
const port = normalizePort(process.env.PORT || "3002");
app.set("port", port);

//Gestionnaire d'erreurs
const errorHandler = (error) => {
  if (error.syscall !== "listen") {
    throw error;
  }
  const address = server.address();
  const bind =
    typeof address === "string" ? "pipe " + address : "port: " + port;
  switch (error.code) {
    case "EACCES":
      console.error(bind + " requires elevated privileges.");
      process.exit(1);
      break;
    case "EADDRINUSE":
      console.error(bind + " is already in use.");
      process.exit(1);
      break;
    default:
      throw error;
  }
};

//Methode createServer: en argument la fonction appelée à chaque requête reçue
const server = http.createServer(app);

server.on("error", errorHandler);
server.on("listening", () => {
  const address = server.address();
  const bind = typeof address === "string" ? "pipe " + address : "port " + port;
  console.log("Listening on " + bind);
});

//le serveur écoute les requêtes sur port 3000 ou autre
server.listen(port);
